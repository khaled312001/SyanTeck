    /**
     * قائمة الأسعار الوهمية
     */
    private $fakePrices = [100, 120, 150, 180, 200, 250, 300, 350, 400, 450, 500];
    private $fakeOnlinePrices = [150, 180, 200, 250, 300, 350, 400, 450, 500, 550, 600];
    private $fakeDeliveryDays = [1, 2, 3, 4, 5, 7];
    private $fakeRevisions = [1, 2, 3, 4];

    /**
     * Run the database seeds.
     *
     * @return void
     */
    public function run()
    {
        $this->command->info('جاري إضافة البيانات الوهمية لجميع الخدمات الموجودة...');
        $this->command->newLine();

        // الحصول على جميع الخدمات الموجودة
        $services = Service::all();
        $totalServices = $services->count();

        if ($totalServices == 0) {
            $this->command->warn('لا توجد خدمات في قاعدة البيانات!');
            return;
        }

        $this->command->info("تم العثور على {$totalServices} خدمة في قاعدة البيانات.");
        $this->command->newLine();

        $updated = 0;
        $skipped = 0;
        $descriptionUpdated = 0;

        // شريط التقدم
        $bar = $this->command->getOutput()->createProgressBar($totalServices);
        $bar->start();

        foreach ($services as $service) {
            $updateData = [];
            $needsUpdate = false;

            // تحديث الوصف - دائماً نحدثه لضمان أنه أكثر من 150 حرف
            $currentDescription = strip_tags($service->description ?? '');
            $currentDescriptionLength = mb_strlen($currentDescription, 'UTF-8');
            
            if (empty($currentDescription) || $currentDescriptionLength < 150) {
                $randomDescription = $this->fakeDescriptions[array_rand($this->fakeDescriptions)];
                $updateData['description'] = $randomDescription;
                $needsUpdate = true;
                $descriptionUpdated++;
            }

            // تحديث السعر إذا كان null أو 0
            if (empty($service->price) || $service->price == 0) {
                $updateData['price'] = $this->fakePrices[array_rand($this->fakePrices)];
                $needsUpdate = true;
            }

            // تحديث نوع الخدمة إذا كان null
            if (is_null($service->is_service_online)) {
                $updateData['is_service_online'] = rand(0, 1); // عشوائي بين أونلاين وأوفلاين
                $needsUpdate = true;
            }

            // تحديث سعر الخدمة الأونلاين إذا كان null أو 0
            if (is_null($service->online_service_price) || $service->online_service_price == 0) {
                $updateData['online_service_price'] = $this->fakeOnlinePrices[array_rand($this->fakeOnlinePrices)];
                $needsUpdate = true;
            }

            // تحديث أيام التسليم إذا كان null أو 0
            if (is_null($service->delivery_days) || $service->delivery_days == 0) {
                $updateData['delivery_days'] = $this->fakeDeliveryDays[array_rand($this->fakeDeliveryDays)];
                $needsUpdate = true;
            }

            // تحديث عدد المراجعات إذا كان null أو 0
            if (is_null($service->revision) || $service->revision == 0) {
                $updateData['revision'] = $this->fakeRevisions[array_rand($this->fakeRevisions)];
                $needsUpdate = true;
            }

            // تحديث slug إذا كان فارغاً
            if (empty($service->slug)) {
                $updateData['slug'] = Str::slug($service->title);
                $needsUpdate = true;
            }

            // تحديث status إذا كان null
            if (is_null($service->status)) {
                $updateData['status'] = 1; // مفعّل
                $needsUpdate = true;
            }

            // تحديث is_service_on إذا كان null
            if (is_null($service->is_service_on)) {
                $updateData['is_service_on'] = 1; // مفعّل
                $needsUpdate = true;
            }

            // تحديث featured إذا كان null
            if (is_null($service->featured)) {
                $updateData['featured'] = rand(0, 1); // عشوائي
                $needsUpdate = true;
            }

            // تحديث view إذا كان null أو 0
            if (is_null($service->view) || $service->view == 0) {
                $updateData['view'] = rand(10, 1000); // عدد مشاهدات وهمي
                $needsUpdate = true;
            }

            // تحديث tax إذا كان null
            if (is_null($service->tax)) {
                $updateData['tax'] = 0;
                $needsUpdate = true;
            }

            // تحديث is_service_all_cities إذا كان null
            if (is_null($service->is_service_all_cities)) {
                $updateData['is_service_all_cities'] = rand(0, 1);
                $needsUpdate = true;
            }

            if ($needsUpdate) {
                try {
                    Service::where('id', $service->id)->update($updateData);
                    $updated++;
                } catch (\Exception $e) {
                    $this->command->error("خطأ في تحديث الخدمة ID: {$service->id} - {$e->getMessage()}");
                }
            } else {
                $skipped++;
            }

            $bar->advance();
        }

        $bar->finish();
        $this->command->newLine(2);

        // عرض النتائج
        $this->command->info('تم الانتهاء من إضافة البيانات الوهمية!');
        $this->command->newLine();
        $this->command->info("النتائج:");
        $this->command->info("- إجمالي الخدمات: {$totalServices}");
        $this->command->info("- تم تحديث: {$updated} خدمة");
        $this->command->info("- تم تحديث الوصف: {$descriptionUpdated} خدمة (جميعها الآن أكثر من 150 حرف)");
        $this->command->info("- تم تخطي: {$skipped} خدمة (لا تحتاج تحديث)");
        $this->command->newLine();
    }
